package com.sangupta.neo;

public class NeoRuntimeException extends RuntimeException {

    /**
     * Generated via Eclipse
     */
    private static final long serialVersionUID = -1331987126800077564L;

    public NeoRuntimeException() {
        super();
    }
    
    public NeoRuntimeException(String message) {
        super(message);
    }
    
    public NeoRuntimeException(String message, Throwable cause) {
        super(message, cause);
    }
    
    public NeoRuntimeException(Throwable cause) {
        super(cause);
    }
    
}
